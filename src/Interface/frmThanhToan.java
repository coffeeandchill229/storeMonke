    private void ShowData(String MaHD){
        ResultSet rs = null;
        try {
            rs = TT.getData(MaHD);
            while(rs.next()){
                String rows[] = new String[5];
                rows[0] = rs.getString("MaMon");
                rows[1] = rs.getString("TenMon");
                rows[2] = rs.getString("DonGia");
                rows[3] = rs.getString("SoLuong");
                rows[4] = rs.getString("ThanhTien");
                tableModel.addRow(rows);
            }
            ShowText(MaHD);
        } catch (Exception e) {
            System.out.println("Err : " + e.getMessage());
        }
    }
    private void ShowText(String MaHD){
        ResultSet rs = null;
        try {
            rs = TT.getThongTin(MaHD);
            if(rs.next()){
                lbTenKH.setText(rs.getString("TenKH"));
                lbTenNV.setText(rs.getString("TenNV"));
                lbNgayHD.setText(rs.getString("NgayHD"));
                lbTongTien.setText(rs.getString("TongTien"));
            }
        } catch (Exception e) {
            System.out.println("Err : " + e.getMessage());
        }
    }
